package opcionOperacionGeometriaEuclidianaUsuario;

public class DiagonalYarea {
	private String diagonalConocida;
	private String area;
	
	public DiagonalYarea(String diagonalConocida, String area) {
		super();
		this.diagonalConocida = diagonalConocida;
		this.area = area;
	}

	public String getDiagonalConocida() {
		return diagonalConocida;
	}

	public void setDiagonalConocida(String diagonalConocida) {
		this.diagonalConocida = diagonalConocida;
	}

	public String getArea() {
		return area;
	}

	public void setArea(String area) {
		this.area = area;
	}
	

}
