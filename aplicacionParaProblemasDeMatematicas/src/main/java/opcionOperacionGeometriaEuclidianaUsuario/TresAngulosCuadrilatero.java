package opcionOperacionGeometriaEuclidianaUsuario;

public class TresAngulosCuadrilatero {
	private String angulo1;
	private String angulo2;
	private String angulo3;
	
	public TresAngulosCuadrilatero(String angulo1, String angulo2, String angulo3) {
		super();
		this.angulo1 = angulo1;
		this.angulo2 = angulo2;
		this.angulo3 = angulo3;
	}

	public String getAngulo1() {
		return angulo1;
	}

	public void setAngulo1(String angulo1) {
		this.angulo1 = angulo1;
	}

	public String getAngulo2() {
		return angulo2;
	}

	public void setAngulo2(String angulo2) {
		this.angulo2 = angulo2;
	}

	public String getAngulo3() {
		return angulo3;
	}

	public void setAngulo3(String angulo3) {
		this.angulo3 = angulo3;
	}

}
