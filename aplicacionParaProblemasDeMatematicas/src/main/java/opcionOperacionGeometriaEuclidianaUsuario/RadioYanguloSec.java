package opcionOperacionGeometriaEuclidianaUsuario;

public class RadioYanguloSec {
	private String radio;
	private String anguloSec;
	
	public RadioYanguloSec(String radio, String anguloSec) {
		super();
		this.radio = radio;
		this.anguloSec = anguloSec;
	}

	public String getRadio() {
		return radio;
	}

	public void setRadio(String radio) {
		this.radio = radio;
	}

	public String getAnguloSec() {
		return anguloSec;
	}

	public void setAnguloSec(String anguloSec) {
		this.anguloSec = anguloSec;
	}
	
	

}
