package opcionOperacionGeometriaEuclidianaUsuario;

public class DosAngulosTriangulo {
	private String angulo1;
	private String angulo2;
	
	public DosAngulosTriangulo(String angulo1, String angulo2) {
		super();
		this.angulo1 = angulo1;
		this.angulo2 = angulo2;
	}

	public String getAngulo1() {
		return angulo1;
	}

	public void setAngulo1(String angulo1) {
		this.angulo1 = angulo1;
	}

	public String getAngulo2() {
		return angulo2;
	}

	public void setAngulo2(String angulo2) {
		this.angulo2 = angulo2;
	}
	

}
