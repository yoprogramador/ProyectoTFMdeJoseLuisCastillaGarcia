package opcionOperacionGeometriaEuclidianaUsuario;

public class AreaNumLadosLongLado {
	private String area;
	private String longLado;
	private String numLados;
	
	public AreaNumLadosLongLado(String area, String longLado, String numLados) {
		super();
		this.area = area;
		this.longLado = longLado;
		this.numLados = numLados;
	}

	public String getArea() {
		return area;
	}

	public void setArea(String area) {
		this.area = area;
	}

	public String getLongLado() {
		return longLado;
	}

	public void setLongLado(String longLado) {
		this.longLado = longLado;
	}

	public String getNumLados() {
		return numLados;
	}

	public void setNumLados(String numLados) {
		this.numLados = numLados;
	}
	

}
