package opcionOperacionGeometriaEuclidianaUsuario;

public class BaseYarea {
	private String base;
	private String area;
	
	public BaseYarea(String base, String area) {
		super();
		this.base = base;
		this.area = area;
	}

	public String getBase() {
		return base;
	}

	public void setBase(String base) {
		this.base = base;
	}

	public String getArea() {
		return area;
	}

	public void setArea(String area) {
		this.area = area;
	}
	
	

}
