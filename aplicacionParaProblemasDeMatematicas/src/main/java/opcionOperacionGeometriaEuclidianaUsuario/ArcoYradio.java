package opcionOperacionGeometriaEuclidianaUsuario;

public class ArcoYradio {
	private String longitudArco;
	private String longitudRadio;
	
	public ArcoYradio(String longitudArco, String longitudRadio) {
		super();
		this.longitudArco = longitudArco;
		this.longitudRadio = longitudRadio;
	}

	public String getLongitudArco() {
		return longitudArco;
	}

	public void setLongitudArco(String longitudArco) {
		this.longitudArco = longitudArco;
	}

	public String getLongitudRadio() {
		return longitudRadio;
	}

	public void setLongitudRadio(String longitudRadio) {
		this.longitudRadio = longitudRadio;
	}
	

}
