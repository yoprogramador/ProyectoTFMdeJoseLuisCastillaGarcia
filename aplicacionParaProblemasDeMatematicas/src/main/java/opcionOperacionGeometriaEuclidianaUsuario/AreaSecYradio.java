package opcionOperacionGeometriaEuclidianaUsuario;

public class AreaSecYradio {
	private String areaSec;
	private String radio;
	
	public AreaSecYradio(String areaSec, String radio) {
		super();
		this.areaSec = areaSec;
		this.radio = radio;
	}

	public String getAreaSec() {
		return areaSec;
	}

	public void setAreaSec(String areaSec) {
		this.areaSec = areaSec;
	}

	public String getRadio() {
		return radio;
	}

	public void setRadio(String radio) {
		this.radio = radio;
	}
	
	

}
