package opcionOperacionGeometriaEuclidianaUsuario;

public class DosDiagonales {
	private String diagonalMayor;
	private String diagonalMenor;
	
	public DosDiagonales(String diagonalMayor, String diagonalMenor) {
		super();
		this.diagonalMayor = diagonalMayor;
		this.diagonalMenor = diagonalMenor;
	}

	public String getDiagonalMayor() {
		return diagonalMayor;
	}

	public void setDiagonalMayor(String diagonalMayor) {
		this.diagonalMayor = diagonalMayor;
	}

	public String getDiagonalMenor() {
		return diagonalMenor;
	}

	public void setDiagonalMenor(String diagonalMenor) {
		this.diagonalMenor = diagonalMenor;
	}
	

}
