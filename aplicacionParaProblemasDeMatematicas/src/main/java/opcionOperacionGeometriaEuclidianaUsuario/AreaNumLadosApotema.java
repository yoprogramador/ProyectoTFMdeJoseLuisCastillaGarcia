package opcionOperacionGeometriaEuclidianaUsuario;

public class AreaNumLadosApotema {
	private String area;
	private String numLados;
	private String apotema;
	
	public AreaNumLadosApotema(String area, String numLados, String apotema) {
		super();
		this.area = area;
		this.numLados = numLados;
		this.apotema = apotema;
	}

	public String getArea() {
		return area;
	}

	public void setArea(String area) {
		this.area = area;
	}

	public String getNumLados() {
		return numLados;
	}

	public void setNumLados(String numLados) {
		this.numLados = numLados;
	}

	public String getApotema() {
		return apotema;
	}

	public void setApotema(String apotema) {
		this.apotema = apotema;
	}
	

}
