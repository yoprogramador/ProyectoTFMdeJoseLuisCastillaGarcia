package opcionOperacionGeometriaEuclidianaUsuario;

public class AreaYanguloSec {
	private String areaSec;
	private String anguloSec;
	
	public AreaYanguloSec(String areaSec, String anguloSec) {
		super();
		this.areaSec = areaSec;
		this.anguloSec = anguloSec;
	}

	public String getAreaSec() {
		return areaSec;
	}

	public void setAreaSec(String areaSec) {
		this.areaSec = areaSec;
	}

	public String getAnguloSec() {
		return anguloSec;
	}

	public void setAnguloSec(String anguloSec) {
		this.anguloSec = anguloSec;
	}
	
	
	

}
