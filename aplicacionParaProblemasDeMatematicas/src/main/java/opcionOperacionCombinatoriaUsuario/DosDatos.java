package opcionOperacionCombinatoriaUsuario;

public class DosDatos {
	private String n;
	private String r;
	
	public DosDatos(String n, String r) {
		super();
		this.n = n;
		this.r = r;
	}

	public String getN() {
		return n;
	}

	public void setN(String n) {
		this.n = n;
	}

	public String getR() {
		return r;
	}

	public void setR(String r) {
		this.r = r;
	}
	

}
