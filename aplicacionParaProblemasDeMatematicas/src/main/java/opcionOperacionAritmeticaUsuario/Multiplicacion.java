package opcionOperacionAritmeticaUsuario;

public class Multiplicacion {
	private String primerFactor;
	private String segundoFactor;
	
	public Multiplicacion(String primerFactor, String segundoFactor) {
		super();
		this.primerFactor = primerFactor;
		this.segundoFactor = segundoFactor;
	}

	public String getPrimerFactor() {
		return primerFactor;
	}

	public void setPrimerFactor(String primerFactor) {
		this.primerFactor = primerFactor;
	}

	public String getSegundoFactor() {
		return segundoFactor;
	}

	public void setSegundoFactor(String segundoFactor) {
		this.segundoFactor = segundoFactor;
	}
	
	

}
