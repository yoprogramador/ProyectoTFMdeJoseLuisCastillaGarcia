package opcionOperacionAritmeticaUsuario;

public class Potenciacion {
	private String base;
	private String exponente;
	
	public Potenciacion(String base, String exponente) {
		super();
		this.base = base;
		this.exponente = exponente;
	}
	public String getBase() {
		return base;
	}
	public void setBase(String base) {
		this.base = base;
	}
	public String getExponente() {
		return exponente;
	}
	public void setExponente(String exponente) {
		this.exponente = exponente;
	}
	

}
