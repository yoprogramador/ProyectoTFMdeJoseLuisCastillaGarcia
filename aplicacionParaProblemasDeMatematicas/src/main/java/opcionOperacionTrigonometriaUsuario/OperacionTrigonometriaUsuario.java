package opcionOperacionTrigonometriaUsuario;

public class OperacionTrigonometriaUsuario {
	private String operacionTrigonometria;

	public OperacionTrigonometriaUsuario(String operacionTrigonometria) {
		super();
		this.operacionTrigonometria = operacionTrigonometria;
	}

	public String getOperacionTrigonometria() {
		return operacionTrigonometria;
	}

	public void setOperacionTrigonometria(String operacionTrigonometria) {
		this.operacionTrigonometria = operacionTrigonometria;
	}
	

}
